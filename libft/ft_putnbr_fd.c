#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char			c;
	long long int	nbr;

	nbr = n;
	if (nbr < 0)
	{
		write(fd, "-", 1);
		nbr *= -1;
	}
	if (nbr > 9)
	{
		ft_putnbr_fd((nbr / 10), fd);
		ft_putnbr_fd((nbr % 10), fd);
	}
	else
	{
		c = nbr + '0';
		write(fd, &c, 1);
	}
}
